    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        try{
            try(BufferedWriter out = new BufferedWriter(new FileWriter("SimpanNilai.txt"))){
                String inFile1 = ("Nama : "+jLabel5.getText());
                String inFile2 = ("Nilai Rata-Rata : "+jLabel6.getText());
                String inFile3 = ("Grade : "+jLabel7.getText());
                String inFile4 = ("Nama : "+jLabel8.getText());
                
                out.write(inFile1);
                out.newLine();
                out.write(inFile2);
                out.newLine();
                out.write(inFile3);
                out.newLine();
                out.write(inFile4);
                out.newLine();
                JOptionPane.showMessageDialog(null,"Berhasil di simpan dalam file");
            }
        }catch (IOException ex) {
            System.err.println("Error: "+ex.getMessage());
        }
    }//GEN-LAST:event_btSimpanActionPerformed

