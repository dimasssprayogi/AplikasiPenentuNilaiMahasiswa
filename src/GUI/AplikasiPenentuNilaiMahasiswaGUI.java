    private void btHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHitungActionPerformed
        int uts =Integer.parseInt(fldUts.getText());
        int tm =Integer.parseInt(fldTugas.getText());
        int uas =Integer.parseInt(fldUas.getText());
        
        String nama =fldNama.getText().toUpperCase();
        String Grade;
        String Hasil;
        
        if (uts>=0&&uts<=100&&tm>=0&&tm<=100&&uas>=0&&uas<=100){
            int rata =(uts+tm+uas)/3;
            
            if (rata>=90){
                Grade ="A";
                Hasil ="Lulus";
            }
            else if (rata>=80&&rata<=90){
                Grade ="B";
                Hasil ="Lulus";
            }
            else if (rata>=70&&rata<=79){
                Grade ="C";
                Hasil ="Lulus";
            }
            else{
                Grade ="D";
                Hasil ="Tidak Lulus";
            }
            jLabel5.setText("Nama : "+nama);
            jLabel6.setText("Nilai Rata-Rata : "+String.valueOf(rata));
            jLabel7.setText("Grade : "+Grade);
            jLabel8.setText("Hasil : "+Hasil);
        }
        else{
            JOptionPane.showMessageDialog(null,"Nilai bukan numerik","nilai tidak antara 0-100",JOptionPane.ERROR_MESSAGE);
            fldNama.setText("");
            fldUts.setText("");
            fldTugas.setText("");
            fldUas.setText(""); 
        }
            
    }//GEN-LAST:event_btHitungActionPerformed

